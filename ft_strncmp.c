int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int in;

	in = 0;
	while (s1[in] != '\0' && s2[in] != '\0' && in < n)
	{
		if (s1[in] < s2[in])
			return (-1);
		else if (s1[in] > s2[in])
			return (1);
		in++;
	}
	return (0);
}
