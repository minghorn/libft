#include "libft.h"

void		ft_putnbr(int nb)
{
	char	*nbr;
	int		i;

	i = 0;
	nbr = ft_itoa(nb);
	while (nbr[i] != '\0')
	{
		write(1, &nbr[i], 1);
		i++;
	}
}
